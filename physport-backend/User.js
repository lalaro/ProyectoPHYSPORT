class User {
    constructor(id, name, email, password, age, gender, weight, height, goals, preferences) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.password = password;
        this.age = age;
        this.gender = gender;
        this.weight = weight;
        this.height = height;
        this.goals = goals;
        this.preferences = preferences;
    }
}

